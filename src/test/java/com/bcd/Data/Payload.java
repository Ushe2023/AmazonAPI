package com.bcd.Data;

public class Payload {
		

	private String item;
	private int Number;
	private int Price;
	
	
	public String getName() {
		return item;
	}
	public void setName(String name) {
		item = name;
	}
	public int getNumber() {
		return Number;
	}
	public void setNumber(int number) {
		Number = number;
	}
	public int getPrice() {
		return Price;
	}
	public void setPrice(int price) {
		Price = price;
	}
	
	
}
